package week1.day3;

public class PositiveOrNagative {
	
public static void main(String[] args) {
	int a=-126;
	int b=98;
	if(a>=0) {
		System.out.println(a +"= POSITIVE NUMBER");
	}
	else {
		System.out.println(a +"= NEGATIVE NUMBER");
	}
	if(b>=0) {
		System.out.println(b + "= POSITIVE NUMBER");
	}
	else {
		System.out.println(b +"= NEGATIVE NUMBER");
	}
		
	
}
}
